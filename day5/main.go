package main

import "fmt"

func main() {
	{
		stacks := copyStacks()
		for _, move := range moves {
			amount := move[0]
			// -1 because we need indices starting at 0, not numbers starting at 1.
			from := move[1] - 1
			to := move[2] - 1
			for i := 0; i < amount; i++ {
				stacks[to] = append(stacks[to], stacks[from][len(stacks[from])-1])
				stacks[from] = stacks[from][:len(stacks[from])-1]
			}
		}
		fmt.Print("part 1) ")
		for _, s := range stacks {
			fmt.Print(string(s[len(s)-1]))
		}
		fmt.Println()
	}
	{
		stacks := copyStacks()
		for _, move := range moves {
			amount := move[0]
			// -1 because we need indices starting at 0, not numbers starting at 1.
			from := move[1] - 1
			to := move[2] - 1
			for i := 0; i < amount; i++ {
				stacks[to] = append(stacks[to], stacks[from][len(stacks[from])-amount+i])
			}
			stacks[from] = stacks[from][:len(stacks[from])-amount]
		}
		fmt.Print("part 2) ")
		for _, s := range stacks {
			fmt.Print(string(s[len(s)-1]))
		}
		fmt.Println()
	}
}

func copyStacks() [][]rune {
	var c [][]rune
	for _, s := range stacks {
		c = append(c, copyRunes(s))
	}
	return c
}

func copyRunes(r []rune) []rune {
	c := make([]rune, len(r))
	copy(c, r)
	return c
}

